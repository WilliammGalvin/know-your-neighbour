    <h1>Our Mission</h1>

    <p>
        The goal of “know your neighbour” is to change the way the people of Collingwood perceive the homeless. We have a unique opportunity within a relatively small and close knit community like Collingwood, which is not only to see and recognize the same people on the street, but to know them by name. By knowing the stories and perspectives of those suffering with homelessness in this area we hope that we can see each other not as “us and them” but for what we are, neighbours and fellow members of our community. The documentation of the stories and experiences of Collingwood’s homeless citizens is meant to bridge the lack of understanding and dispel the stigma which surrounds homelessness. The dehumanisation of the homeless is a massive social and cultural issue, caused by a lack of understanding and the perpetuation of negative myths and stereotypes surrounding homeless people. When you see citizens of Collingwood, our neighbours, on the street don’t hesitate to say hello and make conversation the way you might with the family next door. Choose empathy, not apathy.
    </p>
